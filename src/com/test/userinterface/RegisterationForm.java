    private void nameFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nameFieldKeyTyped
        
        char c = evt.getKeyChar();
        System.out.println(c + "h");
        System.out.println(Character.isWhitespace(c));
        System.out.println(Character.isSpaceChar(c));
        System.out.println(Character.toString(c).equals(" "));
        if(!(Character.isLetter(c) || Character.isISOControl(c)|| Character.isWhitespace(c) || Character.isSpaceChar(c) || Character.toString(c).equals(" "))){
        
            evt.consume();
            getToolkit().beep();
            JOptionPane.showMessageDialog(this, "Only Numbers and Letters!");
        }
    }//GEN-LAST:event_nameFieldKeyTyped

