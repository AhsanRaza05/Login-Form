    boolean isValidData(){
    
        if(nameField.getText().trim().isEmpty()){
            
            JOptionPane.showMessageDialog(this, "No Name!");
        }
        //else if(emailField.getText().trim().isEmpty()){
        else if(!LoginJFrame.isValidEmail(emailField.getText().trim())){  
            
            JOptionPane.showMessageDialog(this, "No Email or Invalid Email!");
        }
        //else if(passwordField.getText().trim().isEmpty()){
        else if(!LoginJFrame.isValidPassword(passwordField.getText().trim().toCharArray())){
        
            JOptionPane.showMessageDialog(this, "No Password or Invalid Password!");
        }
        else if(!(maleRadio.isSelected() || femaleRadio.isSelected())){
            
            JOptionPane.showMessageDialog(this, "No Gender!");
        }
        else if(countryCombo.getSelectedItem().toString().equals(countryCombo.getItemAt(0))){
            
            JOptionPane.showMessageDialog(this, "No Department!");
        }     
        else if(countryCombo.getSelectedItem().toString().equals(countryCombo.getItemAt(0))){
            
            JOptionPane.showMessageDialog(this, "No City!");
        }
        else{
            return true;
        }
        return false;
    }

    UserModel getAllUserData(){
        
        String name = nameField.getText().trim();
        String email = emailField.getText().trim();
        String password = passwordField.getText().trim();
        String gender = "";
        
        if(maleRadio.isSelected()){
            gender = (maleRadio.getText());
        }
        else{
            gender = (femaleRadio.getText());
        }
        
        Integer cityId = (new CityDAOImpl().getSpecificCountryId(countryCombo.getSelectedItem().toString().trim()));
       
        UserModel s = new UserModel(name, email, password, gender, cityId);
        System.out.println(s);
        return s;
    }
   
